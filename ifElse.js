// Switch statement
let day;

switch (new Date().getDay()) {
    case 0:
        day= "Sunday"
        
        break;
    case 1:
        day= "Monday"
        
        break;
    case 2:
        day= "Tuesday"
        
        break;
    case 3:
        day= "Wednessday"
        
        break;
    case 4:
        day= "Thursday"
        
        break;
    case 5:
        day = "Friday"
        
        break;

    default:
        day= "Saturday"
        break;
}
console.log(day);









